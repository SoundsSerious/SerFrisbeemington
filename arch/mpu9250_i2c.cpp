void MPU_9250_I2C::initialize()
{
  frisbeem.com.log("Starting I2C");
  Wire.setClock(I2C_SPEED);
  Wire.begin(SDA_PIN, SCL_PIN,I2C_SPEED); // 21 & 22 are default on ESP32


  frisbeem.com.log("Initializing MPU");
  // Set up the interrupt pin, its set as active high, push-pull
  pinMode(intPin, INPUT);
  digitalWrite(intPin, LOW);
  //pinMode(myLed, OUTPUT);
  //digitalWrite(myLed, HIGH);

  delay(100);

  // Read the WHO_AM_I register, this is a good test of communication
  byte c = readByte(MPU9250_ADDRESS, WHO_AM_I_MPU9250);  // Read WHO_AM_I register for MPU-9250
  String message = "MPU9250 I AM 0x"+String(c,HEX)+" I should be 0x71 OR 0x73";
  frisbeem.com.log( message );
  delay(100);

  if (c == 0x71 || c == 0x73) // WHO_AM_I should always be 0x68
  {
     frisbeem.com.log("MPU9250 is online...");

    MPU9250SelfTest(SelfTest); // Start by performing self test and reporting values
   frisbeem.com.log("x-axis self test: acceleration trim within : "); frisbeem.com.log(String(SelfTest[0])); frisbeem.com.log("% of factory value");
   frisbeem.com.log("y-axis self test: acceleration trim within : "); frisbeem.com.log(String(SelfTest[1])); frisbeem.com.log("% of factory value");
   frisbeem.com.log("z-axis self test: acceleration trim within : "); frisbeem.com.log(String(SelfTest[2])); frisbeem.com.log("% of factory value");
   frisbeem.com.log("x-axis self test: gyration trim within : "); frisbeem.com.log(String(SelfTest[3])); frisbeem.com.log("% of factory value");
   frisbeem.com.log("y-axis self test: gyration trim within : "); frisbeem.com.log(String(SelfTest[4])); frisbeem.com.log("% of factory value");
   frisbeem.com.log("z-axis self test: gyration trim within : "); frisbeem.com.log(String(SelfTest[5])); frisbeem.com.log("% of factory value");

    calibrateMPU9250(gyroBias, accelBias); // Calibrate gyro and accelerometers, load biases in bias registers
    delay(1000);

    initMPU9250();
    frisbeem.com.log("MPU9250 initialized for active data mode...."); // Initialize device for active mode read of acclerometer, gyroscope, and temperature

    // Read the WHO_AM_I register of the magnetometer, this is a good test of communication
    byte d = readByte(AK8963_ADDRESS, WHO_AM_I_AK8963);  // Read WHO_AM_I register for AK8963
    delay(1000);

    // Get magnetometer calibration from AK8963 ROM
    initAK8963(magCalibration);
    frisbeem.com.log("AK8963 initialized for active data mode...."); // Initialize device for active mode read of magnetometer

    delay(1000);
  }
  else
  {
    frisbeem.com.log("Could not connect to MPU9250: 0x"+String(c, HEX));
    //while(1) ; // Loop forever if communication doesn't happen
  }
}

void MPU_9250_I2C::update()
{
  // If intPin goes high, all data registers have new data
  if (readByte(MPU9250_ADDRESS, INT_STATUS) & 0x01) {
    frisbeem.com.log("Interrupt"); // On interrupt, check if data ready interrupt
    frisbeem.com.log("Reading ACL");
    readAccelData(accelCount);  // Read the x/y/z adc values
    frisbeem.com.log("Got ACL");
    getAres();
    // Now we'll calculate the accleration value into actual g's
    A.x = (float)accelCount[0]*aRes; // - accelBias[0];  // get actual g value, this depends on scale being set
    A.y = (float)accelCount[1]*aRes; // - accelBias[1];
    A.z = (float)accelCount[2]*aRes; // - accelBias[2];

    frisbeem.com.log("Reading Gyro");
    readGyroData(gyroCount);  // Read the x/y/z adc values
    getGres();
    frisbeem.com.log("Got Gyro");

    // Calculate the gyro value into actual degrees per second
    G.x = (float)gyroCount[0]*gRes;  // get actual gyro value, this depends on scale being set
    G.y = (float)gyroCount[1]*gRes;
    G.z = (float)gyroCount[2]*gRes;

    frisbeem.com.log("Reading MAG");
    readMagData(magCount);  // Read the x/y/z adc values
    getMres();
    frisbeem.com.log("Got MAG");
    magbias[0] = 0;//+100.;  // User environmental x-axis correction in milliGauss, should be automatically calculated
    magbias[1] = 0;//+120.;  // User environmental x-axis correction in milliGauss
    magbias[2] = 0;//-200.;  // User environmental x-axis correction in milliGauss

    // Calculate the magnetometer values in milliGauss
    // Include factory calibration per data sheet and user environmental corrections
    M.x = (float)magCount[0]*mRes*magCalibration[0] - magbias[0];  // get actual magnetometer value, this depends on scale being set
    M.y = (float)magCount[1]*mRes*magCalibration[1] - magbias[1];
    M.z = (float)magCount[2]*mRes*magCalibration[2] - magbias[2];
  }
  else{
    frisbeem.com.log("No Interrupt");
  }

  sum += deltat; // sum for averaging filter update rate
  sumCount++;

  calculatePositionalInformation();
}


